export const flooringConfig = {
  eyebrow: 'Избор на подови настилки',
  helperText: 'Открий най-добрата настилка според натоварването.',
  consultationUrl: '/contact',
  questions: [
    { 
      id: 'room', 
      question: 'За кое помещение избирате настилка?', 
      options: ['Хол / Всекидневна', 'Спалня / Детска', 'Кухня', 'Коридор / Антре', 'Офис / Търговско помещение'] 
    },
    {
      id: 'humidity',
      question: 'Има ли риск от мокрене и разливане на вода?',
      options: ['Висок (често миене, мокри обувки)', 'Умерен (инцидентно разливане)', 'Нисък (само химическо чистене / влажен парцал)'],
      condition: (answers) => ['Кухня', 'Коридор / Антре', 'Офис / Търговско помещение'].includes(answers.room)
    },
    { 
      id: 'comfort', 
      question: 'Какво е най-важно за усещането (комфорта)?', 
      options: ['Топлина и уют (ходене боси)', 'Тишина (обиране на шума)', 'Здравина и лесно почистване'] 
    },
    { 
      id: 'traffic', 
      question: 'Какво е нивото на натоварване в помещението?', 
      options: ['Слабо (предимно почивка)', 'Средно (ежедневни дейности)', 'Силно (достъп с обувки, домашни любимци, мебели на колелца)'] 
    },
    { 
      id: 'budget', 
      question: 'Какъв е бюджетният клас на материала?', 
      options: ['Бюджетно решение', 'Оптимално (Цена/Качество)', 'Премиум (Естествени материали)'] 
    }
  ],
  calculateResult: (answers) => {
    const isWet = answers.humidity === 'Висок (често миене, мокри обувки)';
    const highTraffic = answers.traffic === 'Силно (достъп с обувки, домашни любимци, мебели на колелца)';
    const wantsWarmth = answers.comfort === 'Топлина и уют (ходене боси)';
    const premium = answers.budget === 'Премиум (Естествени материали)';
    const budget = answers.budget === 'Бюджетно решение';

    const isKitchenOrHall = ['Кухня', 'Коридор / Антре'].includes(answers.room);

    let flooringType = 'Ламиниран паркет (Клас 32/AC4)';
    let subfloor = 'Стандартна акустична подложка';
    let bullets = [];

    // Wet and heavy logic
    if (isWet || isKitchenOrHall) {
      if (premium) {
        flooringType = 'LVT / SPC Винилова настилка (висок клас)';
        bullets.push('Винилът (SPC) е 100% водоустойчив и много по-комфортен и топъл от плочките. Перфектен премиум избор за кухни и коридори.');
      } else {
        flooringType = 'Влагоустойчив ламинат (Клас 33/AC5) или Плочки';
        bullets.push('Във влажни зони обикновеният ламинат ще се подуе. Изберете специализиран влагоустойчив ламинат или винил.');
      }
      subfloor = 'Подложка за винил / XPS за влажни помещения';
    } 
    // Bedroom / Living room logic
    else if (premium && wantsWarmth && !highTraffic) {
      flooringType = 'Трислоен или масивен естествен паркет';
      bullets.push('Естественото дърво осигурява ненадминат уют и топлина. Изисква по-внимателна поддръжка, но старее красиво.');
      subfloor = 'Коркова или специализирана PU подложка';
    }
    // High traffic logic
    else if (highTraffic) {
      flooringType = 'Ламиниран паркет (Клас 33/AC5) или SPC Винил';
      if (answers.comfort === 'Тишина (обиране на шума)') {
        flooringType = 'SPC Винилова настилка (клик система)';
        bullets.push('Винилът обира шума от стъпки много по-добре от ламината и е изключително издръжлив на надрасквания.');
      } else {
        bullets.push('За зони с голямо натоварване (столове, обувки) не правете компромис с класа на износоустойчивост (минимум AC5).');
      }
      subfloor = 'Шумоизолираща твърда подложка';
    }
    // General budget logic
    else if (budget) {
      flooringType = 'Ламиниран паркет (Клас 31/AC3 или 32/AC4)';
      bullets.push('За нормална употреба у дома, стандартният ламинат е най-разумният и икономичен избор.');
    }

    if (answers.comfort === 'Тишина (обиране на шума)') {
      subfloor = 'Каучукова или XPS шумоизолираща подложка';
      bullets.push('Над 50% от акустичния комфорт зависи от подложката. Инвестирайте в хубава подложка, дори при по-евтин ламинат.');
    }

    return {
      tiles: [
        { label: 'Препоръчителна настилка', value: flooringType, spanAll: true },
        { label: 'Необходима подложка', value: subfloor },
      ],
      bullets: bullets.slice(0, 3)
    };
  }
};