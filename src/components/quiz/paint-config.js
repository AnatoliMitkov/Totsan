export const paintConfig = {
  eyebrow: 'Избор на бои и мазилки',
  helperText: 'Кратък въпросник за правилен избор.',
  consultationUrl: '/contact',
  questions: [
    { 
      id: 'room', 
      question: 'Какво помещение ще се обработва?', 
      options: ['Баня / Мокро помещение', 'Кухня', 'Хол / Спалня', 'Търговско / Офис', 'Коридор / Стълбище'] 
    },
    {
      id: 'humidity',
      question: 'Има ли директно мокрене или силен конденз?',
      options: ['Да, редовно мокрене', 'Само лек конденз (пара)', 'Не'],
      // Conditionally skip humidity for bedrooms and halls
      condition: (answers) => ['Баня / Мокро помещение', 'Кухня'].includes(answers.room)
    },
    { 
      id: 'traffic', 
      question: 'Какво е очакваното натоварване и цапане?', 
      options: ['Слабо (предимно тавани или спални без деца)', 'Средно (нормално ежедневно ползване)', 'Силно (деца, домашни любимци, клиенти)'] 
    },
    { 
      id: 'walls', 
      question: 'В какво състояние са стените в момента?', 
      options: ['Гладки и чисти', 'С леки неравности и дефекти', 'Проблемни (мухъл, стари петна, лющене)', 'Ново строителство (нужда от подготовка)'] 
    },
    { 
      id: 'priority', 
      question: 'Кое е най-важно за вас в този случай?', 
      options: ['Лесно почистване и възможност за миене', 'Красива декоративна визия и ефект', 'Най-ниска цена', 'Дълготрайност и здравина на покритието'] 
    }
  ],
  calculateResult: (answers) => {
    const isWetRoom = answers.room === 'Баня / Мокро помещение';
    const hasCondensation = answers.humidity && answers.humidity !== 'Не';
    const isDirectWater = answers.humidity === 'Да, редовно мокрене';
    
    const highTraffic = answers.traffic === 'Силно (деца, домашни любимци, клиенти)' || answers.room === 'Търговско / Офис' || answers.room === 'Коридор / Стълбище';
    
    const wantsDeco = answers.priority === 'Красива декоративна визия и ефект';
    const wantsCleanable = answers.priority === 'Лесно почистване и възможност за миене';
    const cheap = answers.priority === 'Най-ниска цена';
    
    const defaults = {
      badWalls: answers.walls === 'Проблемни (мухъл, стари петна, лющене)',
      unevenWalls: answers.walls === 'С леки неравности и дефекти'
    };

    let paintType = 'Висококачествен интериорен латекс';
    let finish = 'Мат (прикрива нормални несъвършенства)';
    let prep = 'Стандартен дълбокопроникващ грунд';
    let bullets = [];

    if (isWetRoom || hasCondensation) {
      paintType = 'Антибактериална боя за влажни помещения (с фунгициди)';
      finish = 'Сатен / Полумат (отблъсква водата)';
      if (isDirectWater && wantsDeco) {
        paintType = 'Влагоустойчив микроцимент система';
        bullets.push('Микроциментът е изключително издръжлив на директно мокрене, което го прави страхотна дизайнерска алтернатива на плочките в банята.');
      } else if (isDirectWater) {
        paintType = 'Специализирана полиуретанова/епоксидна боя';
        bullets.push('В зоните с директно обливане с вода обикновените бои ще поддадат – използвайте система, устойчива на директно мокрене.');
      }
    } else if (wantsDeco) {
      paintType = 'Декоративна мазилка (Венецианска, Травертино или Релефна)';
      finish = 'Според избрания ефект';
    } else if (highTraffic || wantsCleanable) {
      paintType = 'Устойчива миеща се боя (Клас 1 или 2 на изтриване)';
      finish = 'Сатен (понася локално почистване с препарати)';
    } else if (cheap) {
      paintType = 'Стандартен интериорен латекс';
      finish = 'Мат';
    }

    if (defaults.badWalls) {
      prep = 'Антимухъл третиране изолиращ грунд срещу петна';
      bullets.push('Внимание: Боята не лекува мухъл. Задължително е предварително премахване на проблема и специализиран грунд, иначе дефектите ще избият отново.');
    } else if (defaults.unevenWalls) {
      prep = 'Фина изглаждаща шпакловка или релефен грунд';
      if (finish.includes('Сатен') || finish.includes('Полумат')) {
        finish = 'Мат (задължително)';
        bullets.push('Макар и по-труден за миене, матовият финиш ще скрие леките неравности. Гланцовите и сатенени бои подчертават всеки дефект на стената.');
      } else {
        bullets.push('При дефекти избягвайте лъскави финиши. Матовите бои или релефните мазилки прикриват неравностите по-добре.');
      }
    } else if (answers.walls === 'Ново строителство (нужда от подготовка)') {
      prep = 'Дълбокопроникващ грунд за изравняване на попиваемостта';
      bullets.push('Новите мазилки "пият" боя. Не пестете от грунд – той ще намали разхода ви на боя и ще осигури здрава основа.');
    }

    if (highTraffic && cheap) {
      bullets.push('Евтините бои се зацапват бързо и не търпят търкане. В зони с много движение е по-разумно да инвестирате в по-висок клас интериорна боя, за да не пребоядисвате скоро.');
    }

    bullets.push(`Препоръчваме да започнете задължително със стъпка: ${prep.toLowerCase()}.`);

    return {
      tiles: [
        { label: 'Препоръчителен материал', value: paintType, spanAll: true },
        { label: 'Подходящ финиш', value: finish },
        { label: 'Задължителна подготовка', value: prep }
      ],
      bullets: bullets.slice(0, 4)
    };
  }
};