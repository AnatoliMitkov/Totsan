export const tilesConfig = {
  eyebrow: 'Избор на плочки',
  helperText: 'Открий точния материал според помещението.',
  consultationUrl: '/contact',
  questions: [
    { 
      id: 'location', 
      question: 'Къде ще се полагат плочките?', 
      options: ['Вътре (Баня / Кухня / Коридор)', 'Вън (Тераса / Двор / Стълби)'] 
    },
    {
      id: 'room',
      question: 'За кое помещение става въпрос?',
      options: ['Баня / Мокро помещение', 'Кухня (гръб или под)', 'Коридор / Антре', 'Всекидневна'],
      condition: (answers) => answers.location === 'Вътре (Баня / Кухня / Коридор)'
    },
    {
      id: 'humidity',
      question: 'Ще има ли директно мокрене или задържане на вода?',
      options: ['Да (душ зона, открита тераса)', 'Частично (около мивка/кухня)', 'Не'],
    },
    { 
      id: 'slip', 
      question: 'Търсите ли специална защита против хлъзгане (anti-slip)?', 
      options: ['Задължително (деца, възрастни хора, външни условия)', 'Препоръчително, но не критично', 'Търся по-гладка повърхност (лесна за чистене)'] 
    },
    { 
      id: 'traffic', 
      question: 'Какво е очакваното натоварване на настилката?', 
      options: ['Слабо (предимно боси/по пантофи)', 'Средно (коридор, кухня)', 'Силно (търговски обект / външен двор със стъпки)'] 
    }
  ],
  calculateResult: (answers) => {
    const isOutdoor = answers.location === 'Вън (Тераса / Двор / Стълби)';
    const isBathroom = answers.room === 'Баня / Мокро помещение';
    const isWet = answers.humidity === 'Да (душ зона, открита тераса)';
    const needsAntiSlip = answers.slip === 'Задължително (деца, възрастни хора, външни условия)' || isOutdoor;
    const highTraffic = answers.traffic === 'Силно (търговски обект / външен двор със стъпки)';

    let materialType = 'Гранитогрес (стандартен матов)';
    let finish = 'Мат (R9-R10 клас против хлъзгане)';
    let bullets = [];

    // Outdoor logic
    if (isOutdoor) {
      if (highTraffic) {
        materialType = 'Технически гранитогрес (2см дебелина / усилен)';
      } else {
        materialType = 'Мразоустойчив гранитогрес';
      }
      finish = 'Релефен / Anti-slip (R11-R12 клас)';
      bullets.push('За външни условия задължително изисквайте мразоустойчив гранитогрес. Обикновеният теракот ще се напука от студа.');
      bullets.push('Високият клас против хлъзгане (R11+) е критичен навън, особено при дъжд или сняг.');
    } 
    // Indoor logic
    else {
      if (isBathroom && isWet) {
        if (needsAntiSlip) {
          materialType = 'Гранитогрес (подов) + Фаянс (стенен)';
          finish = 'Мат / Релеф (R10-R11) за пода, Гланц/Мат за стените';
          bullets.push('Честа грешка е полагането на хлъзгави полирани плочки на пода в банята. Задължително изберете матов или anti-slip завършек за душ зоната.');
        } else {
          materialType = 'Гранитогрес / Фаянс';
          finish = 'Мат (по-лесен за почистване)';
        }
      } else if (answers.room === 'Кухня (гръб или под)') {
        materialType = 'Гранитогрес (за под) или Керамика/Фаянс (за гръб)';
        finish = answers.slip === 'Търся по-гладка повърхност (лесна за чистене)' ? 'Мат или Полиран (лесно почистване)' : 'Мат';
        bullets.push('За кухненски гръб може да ползвате полирани плочки, но за пода препоръчваме мат, за да скрива прах и да не се хлъзга при намокряне.');
      } else if (highTraffic) {
        materialType = 'Калиброван (безфугов) гранитогрес';
        finish = 'Натурален мат (висока износоустойчивост)';
        bullets.push('За помещения с висок трафик избягвайте гланцираните плочки – те се драскат лесно от песъчинки и губят блясъка си.');
      } else {
        materialType = 'Стандартен теракот или гранитогрес';
        finish = 'Според дизайна (Полиран / Мат / Сатен)';
      }
    }

    bullets.push('Доверете се на качествен гъвкав фибро-полимерен клей (лепило), особено при големи формати и външни условия.');

    return {
      tiles: [
        { label: 'Препоръчителен материал', value: materialType, spanAll: true },
        { label: 'Подходящ финиш / Повърхност', value: finish },
      ],
      bullets: bullets.slice(0, 3)
    };
  }
};